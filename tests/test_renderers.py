    assert "`raidel-coder`" in rendered.content
    assert "`raidel-scout`" in rendered.content
    assert "`raidel-auditor`" in rendered.content
    assert "`raidel-researcher`" in rendered.content
    assert "`raidel-planner`" not in rendered.content


