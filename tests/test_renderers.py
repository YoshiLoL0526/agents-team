def test_renders_claude_markdown() -> None:
    agent = parse_agent_file(Path("agents/raidel-auditor.md"))
    rendered = render_agent(agent, "claude")

    assert rendered.filename == "raidel-auditor.md"
    assert rendered.content.startswith("---\n# Generated by Agents Team")
    frontmatter = rendered.content.split("---", 2)[1]
    data = yaml.safe_load(frontmatter)
    assert data["name"] == "raidel-auditor"
    assert data["model"] == "sonnet"
    assert data["tools"] == "Read, Glob, Grep, Bash"
    assert set(data) == {"name", "description", "model", "tools"}


def test_renders_opencode_markdown() -> None:
    agent = parse_agent_file(Path("agents/raidel-researcher.md"))
    rendered = render_agent(agent, "opencode")

    assert rendered.filename == "raidel-researcher.md"
    assert rendered.content.startswith("---\n")
