def test_renders_opencode_raidel_planner_as_primary_agent() -> None:
    agent = parse_agent_file(Path("agents/raidel-planner.md"))
    rendered = render_agent(agent, "opencode")

    assert rendered.filename == "raidel-planner.md"
    frontmatter = rendered.content.split("---", 2)[1]
    data = yaml.safe_load(frontmatter)
    assert data["mode"] == "primary"
    assert data["model"] == "openai/gpt-5.4"
    assert data["permission"]["task"] == {
        "*": "deny",
        "raidel-coder": "allow",
        "raidel-scout": "allow",
        "raidel-researcher": "allow",
        "raidel-auditor": "allow",
    }


def test_renders_opencode_agents_as_subagents_by_default() -> None:
    agent = parse_agent_file(Path("agents/raidel-auditor.md"))
    agent_without_override = replace(agent, overrides={})

    rendered = render_agent(agent_without_override, "opencode")

    frontmatter = rendered.content.split("---", 2)[1]
    data = yaml.safe_load(frontmatter)
    assert data["mode"] == "subagent"
